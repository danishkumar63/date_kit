/// An enumeration representing various time difference formats.
enum TimeDifferenceFormat {
  /// Format: minutes and seconds (MM:SS)
  fMMSS,

  /// Format: hours and minutes (HH:MM)
  fHHMM,

  /// Format: hours, minutes, and seconds (HH:MM:SS)
  fHHMMSS,

  /// Format: days and hours (DD:HH)
  fDDHH,

  /// Format: days, hours, and minutes (DD:HH:MM)
  fDDHHMM,

  /// Format: days, hours, minutes, and seconds (DD:HH:MM:SS)
  fDDHHMMSS,
}
